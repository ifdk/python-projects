def num_words(sentence=""):
    split_word = len(sentence.split())
    return split_word


print(num_words("this is a cat"))
print(num_words("this"))
print(num_words(""))
print(num_words(""))
print(num_words("75098 75287 75230 75231 75236 75237 75235 75254 75251 75252 75253 75043 75032 75227"))
